// example to find out average of 5 numbers

class Demo4{
    public static void main(String args[]){
        int a = 5, b = 2, c = 3, d = 2, e = 1;
        double avg = (a+b+c+d+e)/5.0;
        System.out.println("Average of 5 Numbers : "+avg);
        System.out.printf("Average of 5 Numbers : %.3f\n",avg);
    }
}