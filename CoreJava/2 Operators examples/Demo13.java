// example showing the concept of arithmetic assignment operator

class Demo13{
    public static void main(String args[]){
        int a = 5;
        a += 100; // a = a  + 100;
        System.out.println("a : "+a);

        int b = 100;
        b *= 4; // b = b * 4;
        System.out.println("b : "+b);        
    }
}
